import java.util.*;
public class Main
{
	public static void main(String[] args) {
		for(int i=0;i<5;i++)
		{
		    for(int j=1;j<i+2;j++)
		
		    {
		        System.out.print(j);
		    }
		    System.out.print("\n");
		}
		System.out.print("\n");
		for(int i=1;i<=5;i++)
		{
		    
		    for(int j=5;j>=i;j--)
		
		    {
		        System.out.print(j);
		       
		    }
		    System.out.print("\n");
		}
		
	}
}
